package PSF::Server::Comms::Protocol;

# ============================================================
sub new {
# ============================================================
	my ($class) = map { ref || $_ } shift;
	my $self = bless {}, $class;
	$self->init( @_ );
	return $self;
}

# ============================================================
sub init {
# ============================================================
	my $self   = shift;
	my $server = shift;

	$self->{ _server } = $server;
}

# ============================================================
sub send {
# ============================================================
	my $self = shift;
	return $self->server->send();
}

# ============================================================
sub server {
# ============================================================
	my $self = shift;
	return $self->{ _server };
}

1;
